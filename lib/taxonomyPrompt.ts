/**
 * 대주제 1개용 주제 트리 생성 "출력 계약" 템플릿.
 *
 * 온보딩 생성은 대주제 단위로 쪼개 claude를 호출한다(진행률 표기 + 요청당 짧게).
 * 즉석 프롬프팅은 호출마다 형식이 흔들리므로, 스키마·규칙·레벨 정의를 고정한
 * 이 템플릿으로 출력을 최대한 일관되게 만든다.
 */
export function majorTopicPrompt(name: string, levels: number[]): string {
  const lv = [...levels].sort((a, b) => a - b).join(", ");
  return `너는 기술 학습 커리큘럼(주제 트리) 설계 도구다. 아래 "출력 계약"을 반드시 지켜라.

## 대상 대주제
- 이름: ${name}
- 레벨: [${lv}]

## 레벨 정의
- 레벨1 = 초급(입문, 개념 이해)
- 레벨2 = 중급(실무 1~3년, 적용과 트레이드오프)
- 레벨3 = 고급(10년차 실무/대규모 시스템 설계, 내부 동작·실패 모드·엣지케이스)

## 생성 규칙 (항상 동일하게 적용)
- 지정된 레벨 범위를 모두 아우른다 (여러 레벨이면 초급~고급 사다리).
- 중주제(mid) 3~6개. 각 중주제의 소주제(sub) 5~10개.
- 소주제는 구체적이고 딥다이브 가능한 항목. (예: "트랜잭션 격리 수준과 이상 현상", "B-Tree vs LSM-Tree 인덱스 내부구조")
- 배열 순서는 항상 쉬운→어려운 학습 사다리 순.
- 한국어 명사구로 간결하게. 완결 문장·마침표 금지.

## 출력 계약 (엄수)
- 아래 JSON "하나"만 출력한다. 설명·주석·코드펜스·앞뒤 텍스트 절대 금지.
- 구조와 키를 정확히 이 형태로 (major 값은 위 대상 이름 그대로):
{"major":"${name}","mids":[{"mid":"중주제명","subs":["소주제1","소주제2"]}]}`;
}
